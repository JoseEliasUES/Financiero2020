    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        double abono = Double.parseDouble(spAbono.getValue().toString());
        datosAbono.get(0).setAbono(abono);
        Calendar fecha = new GregorianCalendar();
        int day = fecha.get(Calendar.DAY_OF_MONTH);
        int month = fecha.get(Calendar.MONTH);
        int year = fecha.get(Calendar.YEAR);
        String dd = day + "-" + month + "-" + year;
        Date date = null;
        try {
            date = new SimpleDateFormat("dd-MM-yyyy").parse(dd);
        } catch (ParseException ex) {
            Logger.getLogger(DetalleCompra.class.getName()).log(Level.SEVERE, null, ex);
        }
        java.sql.Date date2 = new java.sql.Date(date.getTime());
        datosAbono.get(0).setFecha(date2);
        //datosAbono.get(0).setHora();
        Calendar calendar = Calendar.getInstance();
        calendar.setTime(date);
        calendar.add(Calendar.DAY_OF_YEAR, 30);
        datosAbono.get(0).setDescripcion("Abono");
        try {
            daoAbo.GuardarAbono(datosAbono.get(0));
        } catch (SQLException ex) {
            Logger.getLogger(NuevoAbono.class.getName()).log(Level.SEVERE, null, ex);
        }
        dispose();
        CxcAbonos cxc = new CxcAbonos(factura);
        cxc.setVisible(true);
    }//GEN-LAST:event_btnGuardarActionPerformed

