    public NuevoAbono(String factura) {
        initComponents();
        this.setLocationRelativeTo(null);
        this.factura = factura;
        daoAbo = new DAO_Abono();
        try {
            datosAbono = daoAbo.getDatosClienteParaAbono(factura);
            txtFact.setText(factura);
            txtMora.setText(String.valueOf(datosAbono.get(0).getMora()));
            txtCuota.setText(String.valueOf(datosAbono.get(0).getCuota()));
            txtCliente.setText(datosAbono.get(0).getCliente());
            Calendar fecha = new GregorianCalendar();
            int day = fecha.get(Calendar.DAY_OF_MONTH);
            int month = fecha.get(Calendar.MONTH);
            int year = fecha.get(Calendar.YEAR);
            String dd = day + "-" + month + "-" + year;
            Date date = null;
            try {
                date = new SimpleDateFormat("dd-MM-yyyy").parse(dd);
            } catch (ParseException ex) {
                Logger.getLogger(DetalleCompra.class.getName()).log(Level.SEVERE, null, ex);
            }
            jfecha.setDate(date);
        } catch (SQLException ex) {
            Logger.getLogger(NuevoAbono.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

