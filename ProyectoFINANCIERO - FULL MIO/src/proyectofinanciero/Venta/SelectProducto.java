    private void tblProductosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProductosMouseClicked
        // TODO add your handling code here:
        int x = tblProductos.getSelectedRow();
        if (cFre) {
            lblDesc.setText(String.valueOf(Double.parseDouble(tblProductos.getValueAt(x, 3).toString()) * 0.10));
            Double precioU = Double.parseDouble(tblProductos.getValueAt(x, 3).toString());
            lblTotal.setText(String.format("%3.2f", precioU - Double.parseDouble(lblDesc.getText())));
        }
        if (bxMeses.getSelectedIndex() > 0) {
            Double precioU = Double.parseDouble(tblProductos.getValueAt(x, 3).toString());
            double total;
            switch (bxMeses.getSelectedIndex()) {
                case 1:
                    total = (((precioU * 30) / 100) + precioU) - Double.parseDouble(lblDesc.getText());
                    txtCuota.setText(String.format("%3.2f", (total / 12) * Integer.parseInt(spCant.getValue().toString())));
                    txtPrima.setText(String.format("%3.2f", ((total * 10) / 100) * Integer.parseInt(spCant.getValue().toString())));
                    break;
                case 2:
                    total = ((precioU * 50) / 100) + precioU;
                    txtCuota.setText(String.format("%3.2f", (total / 24) * Integer.parseInt(spCant.getValue().toString())));
                    txtPrima.setText(String.format("%3.2f", ((total * 11) / 100) * Integer.parseInt(spCant.getValue().toString())));
                    break;
                case 3:
                    total = ((precioU * 75) / 100) + precioU;
                    txtCuota.setText(String.format("%3.2f", (total / 36) * Integer.parseInt(spCant.getValue().toString())));
                    txtPrima.setText(String.format("%3.2f", ((total * 12) / 100) * Integer.parseInt(spCant.getValue().toString())));
                    break;
            }
        } else {
            txtCuota.setText("0.00");
            txtPrima.setText("0.00");
        }
    }//GEN-LAST:event_tblProductosMouseClicked

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        int x = tblProductos.getSelectedRow();
        int meses= 36;
        if (x == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un producto");
        } else {
            if (cbContado.isSelected()) {
                try {
                    int idProd = Integer.parseInt(tblProductos.getValueAt(x, 0).toString());
                    if (busqProd(idProd)) {
                        if (bxMeses.getSelectedIndex() == 1) {
                            meses = 12;
                        } else {
                            meses = 24;
                        }
                        prodSeleccionado.add(new Producto(producto.getIdProd(), producto.getNombre(), Integer.parseInt(spCant.getValue().toString()), Double.parseDouble(tblProductos.getValueAt(x, 3).toString()), Double.parseDouble(lblDesc.getText())));
                        Menu.v.actualizarProduct();
                        Menu.v.blocCliente();
                        dispose();
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(Empleados.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else if (bxMeses.getSelectedIndex() > 0) {
                try {
                    int idProd = Integer.parseInt(tblProductos.getValueAt(x, 0).toString());
                    if (busqProd(idProd)) {
                        int mes;
                        if (cbContado.isSelected()) {
                            switch (bxMeses.getSelectedIndex()) {
                                case 1:
                                    mes = 12;
                                    break;
                                case 2:
                                    mes = 24;
                                    break;
                                default:
                                    mes = 36;
                                    break;
                            }
                            prodSeleccionado.add(new Producto(producto.getIdProd(), producto.getNombre(), Integer.parseInt(spCant.getValue().toString()), Double.parseDouble(tblProductos.getValueAt(x, 3).toString()), Double.parseDouble(lblDesc.getText()), Double.parseDouble(txtCuota.getText()), Double.parseDouble(txtPrima.getText()), mes));

                        } else {
                            switch (bxMeses.getSelectedIndex()) {
                                case 1:
                                    mes = 12;
                                    break;
                                case 2:
                                    mes = 24;
                                    break;
                                default:
                                    mes = 36;
                                    break;
                            }
                            prodSeleccionado.add(new Producto(producto.getIdProd(), producto.getNombre(), Integer.parseInt(spCant.getValue().toString()), total, Double.parseDouble(lblDesc.getText()), Double.parseDouble(txtCuota.getText()), Double.parseDouble(txtPrima.getText()), mes));
                        }
                        Menu.v.actualizarProduct();
                        Menu.v.blocCliente();
                        dispose();
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(Empleados.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Seleccione meses plazo a pagar.");
            }

        }

    }//GEN-LAST:event_btnAgregarActionPerformed

    private boolean busqProd(int dui) throws SQLException {
        DAO_Productos daoEm = new DAO_Productos();
        for (Producto x : daoEm.getProductos()) {
            if (dui == 0 ? x.getIdProd() == 0 : dui == (x.getIdProd())) {
                producto = x;
                return true;
            }
        }
        return false;
    }

