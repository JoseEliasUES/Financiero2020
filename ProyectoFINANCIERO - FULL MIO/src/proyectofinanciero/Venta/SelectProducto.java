    private void EstabCuota() {
        int x = tblProductos.getSelectedRow();
        if (bxMeses.getSelectedIndex() > 0) {
            Double precioU = Double.parseDouble(tblProductos.getValueAt(x, 3).toString());
            switch (bxMeses.getSelectedIndex()) {
                case 1:
                    if (cFre) {
                        lblDesc.setText(String.format("%3.2f", (((precioU * 30) / 100) + precioU) * 0.1));
                    }
                    total = (((precioU * 30) / 100) + precioU) - Double.parseDouble(lblDesc.getText());
                    txtCuota.setText(String.format("%3.2f", (total / 12) * Integer.parseInt(spCant.getValue().toString())));
                    txtPrima.setText(String.format("%3.2f", ((total * 10) / 100) * Integer.parseInt(spCant.getValue().toString())));
                    break;
                case 2:
                    if (cFre) {
                        lblDesc.setText(String.format("%3.2f", (((precioU * 50) / 100) + precioU) * 0.1));
                    }
                    total = ((precioU * 50) / 100) + precioU;
                    txtCuota.setText(String.format("%3.2f", (total / 24) * Integer.parseInt(spCant.getValue().toString())));
                    txtPrima.setText(String.format("%3.2f", ((total * 11) / 100) * Integer.parseInt(spCant.getValue().toString())));
                    break;
                case 3:
                    if (cFre) {
                        lblDesc.setText(String.format("%3.2f", (((precioU * 75) / 100) + precioU) * 0.1));
                    }
                    total = ((precioU * 75) / 100) + precioU;
                    txtCuota.setText(String.format("%3.2f", (total / 36) * Integer.parseInt(spCant.getValue().toString())));
                    txtPrima.setText(String.format("%3.2f", ((total * 12) / 100) * Integer.parseInt(spCant.getValue().toString())));
                    break;
            }
        } else {
            txtCuota.setText("0.00");
            txtPrima.setText("0.00");
            lblDesc.setText("0.00");
        }
    }
