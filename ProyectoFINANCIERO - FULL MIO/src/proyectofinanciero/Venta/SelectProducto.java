    private void cbContadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbContadoActionPerformed
        // TODO add your handling code here:
        if (cbContado.isSelected()) {
            bxMeses.setEnabled(false);
            txtCuota.setEnabled(false);
            txtPrima.setEnabled(false);

            lbTotal.setVisible(true);
            lblTotal.setVisible(true);

            int x = tblProductos.getSelectedRow();
            Double precioU = Double.parseDouble(tblProductos.getValueAt(x, 3).toString());
            lblTotal.setText(String.format("%3.2f", precioU - Double.parseDouble(lblDesc.getText())));
            if (cFre) {
                lblDesc.setText(String.valueOf((Double.parseDouble(tblProductos.getValueAt(x, 3).toString()) * 0.10) * Integer.parseInt(spCant.getValue().toString())));
            }
        } else {
            bxMeses.setEnabled(true);
            txtCuota.setEnabled(true);
            txtPrima.setEnabled(true);
            lbTotal.setVisible(false);
            lblTotal.setVisible(false);
        }
    }//GEN-LAST:event_cbContadoActionPerformed

