    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int a = cmbYear.getSelectedIndex();
        int m = cmbMes.getSelectedIndex();
        if (a != 0 && m != 0) {
            trg = new tablaRInv(anio, mes);
            tblEntradas.setModel(trg);
            trv = new tablaRInv(anio, mes, 10);
            tblSalidas.setModel(trv);
            Calculos();
        } else {
            JOptionPane.showMessageDialog(this, "Seleecione mes y anio");
        }


    }//GEN-LAST:event_jButton1ActionPerformed

    private void btnFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltrarActionPerformed
        // TODO add your handling code here:
        int a = cmbYear.getSelectedIndex();
        int m = cmbMes.getSelectedIndex();
        String prod = txtFP.getText();
        if (prod != null) {
            tiv = new tablaInvEx(prod);
            tblExistencias.setModel(tiv);
        }
        if (a != 0 || m != 0) {
            if (prod != null) {
                trg = new tablaRInv(anio, mes, prod);
                tblEntradas.setModel(trg);
                trv = new tablaRInv(anio, mes, 10, prod);
                tblSalidas.setModel(trv);
                Calculos();
            } else {
                JOptionPane.showMessageDialog(this, "Campo vacio");
            }
        }
    }//GEN-LAST:event_btnFiltrarActionPerformed

