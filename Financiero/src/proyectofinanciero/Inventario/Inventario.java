    private void Calculos() {
        inv = trg.getProductos();
        invV = trv.getProductos();
        double sumaC = 0;
        double sumaV = 0;
        for (modelos.Inventario i : inv) {
            sumaC += i.getPrecioCompra();
        }
        lblEntradas.setText(String.valueOf(sumaC));
        for (modelos.Inventario i : invV) {
            sumaV += i.getPrecioCompra();
        }
        lblSalidas.setText(String.valueOf(sumaV));
    }

