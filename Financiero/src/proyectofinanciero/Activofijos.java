    private boolean busqM(Integer nombre) throws SQLException {
        DAO_Activofijo daoC = new DAO_Activofijo();
        for (Activofijo x : daoC.getLsActivosf()) {
            if (nombre.equals(x.getId_activof())) {
                act = x;
                return true;
            }
        }
        return false;
    }
