    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int selectMar = cMarca.getSelectedIndex();
        String codigo = txtCodigo.getText();
        String descripcion = txtDescripcion.getText();
        String modelo = txtModelo.getText();
        String serie = txtSerie.getText();
        Date fecha = DateAdquisicion.getDate();
        double valor = Double.parseDouble(txtValor.getText());
        int tipo = cmbTipo.getSelectedIndex();
        
        String typ="";
        switch(tipo){
            case 1: typ="Edificaciones"; break; 
            case 2: typ="Maquinaria"; break; 
            case 3: typ="Vehiculos"; break; 
            case 4: typ="Otros"; break; 
        }
        if (selectMar == 0 || txtCodigo.getText().isEmpty()  || txtDescripcion.getText().isEmpty() || !"".equals(modelo) || !"".equals(modelo)
                || !"".equals(serie) || !"".equals(txtValor.getText()) || tipo==0 ) {
            DAO_Activofijo da = new DAO_Activofijo();
            if (!flag) {
                try {
                    da.nuevoActivof(new Activofijo(codigo,descripcion,datosM.get(selectMar - 1),modelo,serie,DateAdquisicion.getDate(),valor,typ));
                } catch (SQLException ex) {
                    Logger.getLogger(Activofijos.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                try {
                    da.updateActivof(new Activofijo(idP,codigo,descripcion,datosM.get(selectMar - 1),modelo,serie,fecha,valor,typ));
                } catch (SQLException ex) {
                    Logger.getLogger(Activofijos.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            limpiar();
            actualizarTabla();
        } else {
            JOptionPane.showMessageDialog(this, "Campos vacios");

        }

    }//GEN-LAST:event_btnEditActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        int x = tblActivosf.getSelectedRow();
        if (x == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un Producto");
        } else {
            idP = Integer.parseInt(tblActivosf.getValueAt(x,0).toString());
            try {
                if (busqM(idP)) {
                    txtCodigo.setText(act.getCodigo());
                    txtDescripcion.setText(act.getDescripcion());
                    txtModelo.setText(act.getModelo());
                    txtSerie.setText(act.getSerie());
                    txtValor.setText(""+act.getValor());
                    DateAdquisicion.setDate(act.getFechaadq());
                    cmbTipo.setSelectedItem(act.getTipo());
                    for (int i = 0; i < datosM.size(); i++) {
                        if (datosM.get(i).getIdMarca() == act.getMarca().getIdMarca()) {
                            cMarca.getModel().setSelectedItem(datosM.get(i).getMarca());
                        }
                    }
                    
                    btnAdd.setEnabled(false);

                }
            } catch (SQLException ex) {
                Logger.getLogger(Proveedores.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

